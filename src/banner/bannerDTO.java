package banner;

public class bannerDTO {
		private int num;
		private String url;
		private String fName;
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
		public String getUrl() {
			return url;
		}
		public void setUrl(String url) {
			this.url = url;
		}
		public String getFname() {
			return fName;
		}
		public void setFname(String fName) {
			this.fName = fName;
		}
		
		
		
}
